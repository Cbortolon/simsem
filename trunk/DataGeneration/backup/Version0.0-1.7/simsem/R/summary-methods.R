setMethod("summary", signature="simConstraint", definition=function(object){
	cat("CONSTRAINT OBJECT\n")
	cat("Type\n")
	print(object@Tag)
	cat("-------------Constraint----------------\n")
	for(i in 1:length(object@Equality)) {
		cat(i, ".\n", sep="")
		print(object@Equality[[i]])
		cat("---------------------------------------\n")
	}
})
